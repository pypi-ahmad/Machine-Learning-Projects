#!/usr/bin/env python
"""
Bootstrap Dataset Configs — Phase 3B-4
========================================
Generate template YAML configs in ``configs/datasets/`` for every
trainable project discovered from modern.py modules.

Usage::

    python scripts/bootstrap_dataset_configs.py           # safe: never overwrites
    python scripts/bootstrap_dataset_configs.py --force    # overwrite existing
    python scripts/bootstrap_dataset_configs.py --dry-run  # preview only
"""

from __future__ import annotations

import argparse
import importlib.util
import sys
import textwrap
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

CONFIGS_DIR = ROOT / "configs" / "datasets"

# ── category → (task, framework, kind, primary_metric) ──────────────────────
_CATEGORY_MAP: dict[str, tuple[str, str, str, str]] = {
    "detection":      ("detect", "ultralytics", "ultralytics_yaml", "map50-95"),
    "pose":           ("pose",   "ultralytics", "ultralytics_yaml", "map50-95"),
    "segmentation":   ("seg",    "ultralytics", "ultralytics_yaml", "map50-95"),
    "classification": ("cls",    "auto",        "auto",             "acc_top1"),
    "obb":            ("obb",    "ultralytics", "ultralytics_yaml", "map50-95"),
}

# Categories that are purely OpenCV / utility — no YOLO model, no dataset
_SKIP_CATEGORIES = {"opencv_utility"}


def _discover_projects() -> list[dict]:
    """Import all modern.py and return project dicts with key, task, category."""
    from core.registry import PROJECT_REGISTRY

    # Import every modern.py to populate the registry
    for p in sorted(ROOT.glob("CV */modern.py")):
        spec = importlib.util.spec_from_file_location(
            f"_boot_{p.parent.name}", str(p),
        )
        if spec and spec.loader:
            mod = importlib.util.module_from_spec(spec)
            try:
                spec.loader.exec_module(mod)
            except Exception:
                pass

    results = []
    for reg_name, cls in sorted(PROJECT_REGISTRY.items()):
        cat = getattr(cls, "category", "opencv_utility")
        if cat in _SKIP_CATEGORIES:
            continue

        # Derive registry project_key from the class-level _key if available,
        # else strip _v2 suffix from the @register name.
        project_key = reg_name.removesuffix("_v2")

        task_info = _CATEGORY_MAP.get(cat)
        if task_info is None:
            continue

        task, framework, kind, primary = task_info
        results.append({
            "project_key": project_key,
            "reg_name": reg_name,
            "task": task,
            "framework": framework,
            "kind": kind,
            "primary": primary,
            "display_name": getattr(cls, "display_name", reg_name),
        })
    return results


def _render_yaml(info: dict) -> str:
    """Render a YAML config string for one project."""
    pk = info["project_key"]
    task = info["task"]
    fw = info["framework"]
    kind = info["kind"]
    primary = info["primary"]
    display = info["display_name"]

    if kind == "ultralytics_yaml":
        dataset_block = textwrap.dedent(f"""\
            dataset:
              name: "{display}"
              kind: "ultralytics_yaml"
              root: "data/{pk}"
              data_yaml: "data/{pk}/data.yaml"
              train: null
              val: null
              test: null
              notes: "Place Ultralytics-format dataset under data/{pk}/"
        """)
    elif kind == "auto" and task == "cls":
        dataset_block = textwrap.dedent(f"""\
            dataset:
              name: "{display}"
              kind: "imagefolder"
              root: "data/{pk}"
              data_yaml: null
              train: "data/{pk}/train"
              val: "data/{pk}/val"
              test: null
              notes: "ImageFolder layout: data/{pk}/train/<class>/*.jpg  data/{pk}/val/<class>/*.jpg"
        """)
    else:
        dataset_block = textwrap.dedent(f"""\
            dataset:
              name: "{display}"
              kind: "custom"
              root: "data/{pk}"
              data_yaml: null
              train: null
              val: null
              test: null
              notes: "TODO: configure dataset paths"
        """)

    yaml_str = textwrap.dedent(f"""\
        # Dataset config for: {display}
        # Auto-generated by bootstrap_dataset_configs.py
        project_key: "{pk}"
        task: "{task}"
        framework: "{fw}"

    """) + dataset_block + textwrap.dedent(f"""\

        metrics:
          primary: "{primary}"
          promote_on_eval: false
    """)
    return yaml_str


def main():
    parser = argparse.ArgumentParser(description="Bootstrap dataset configs")
    parser.add_argument("--force", action="store_true", help="Overwrite existing configs")
    parser.add_argument("--dry-run", action="store_true", help="Print what would be created")
    args = parser.parse_args()

    CONFIGS_DIR.mkdir(parents=True, exist_ok=True)

    projects = _discover_projects()
    created = 0
    skipped = 0

    for info in projects:
        out_path = CONFIGS_DIR / f"{info['project_key']}.yaml"
        exists = out_path.exists()

        if exists and not args.force:
            skipped += 1
            if args.dry_run:
                print(f"  [SKIP] {out_path.relative_to(ROOT)}  (exists)")
            continue

        yaml_text = _render_yaml(info)

        if args.dry_run:
            tag = "OVERWRITE" if exists else "CREATE"
            print(f"  [{tag}] {out_path.relative_to(ROOT)}")
        else:
            out_path.write_text(yaml_text, encoding="utf-8")
            tag = "overwritten" if exists else "created"
            print(f"  [{tag}] {out_path.relative_to(ROOT)}")
            created += 1

    total = created + skipped
    print(f"\nDone: {created} created, {skipped} skipped, {total} total trainable projects")


if __name__ == "__main__":
    main()
