                "def mask_to_yolo_seg(mask_path, image_size):\n",
                "    \"\"\"\n",
                "    Convert binary medical segmentation mask to YOLO polygon format.\n",
                "    Returns [class_id, x1, y1, x2, y2, ...] normalized to 0-1.\n",
                "    \"\"\"\n",
                "    mask = cv2.imread(str(mask_path), cv2.IMREAD_GRAYSCALE)\n",
                "    if mask is None:\n",
                "        return None\n",
                "    \n",
                "    # Threshold to binary\n",
                "    _, mask = cv2.threshold(mask, 1, 255, cv2.THRESH_BINARY)\n",
                "    \n",
                "    # Find contours\n",
                "    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)\n",
                "    \n",
                "    if not contours:\n",
                "        return None\n",
                "    \n",
                "    # Use largest contour\n",
                "    contour = max(contours, key=cv2.contourArea)\n",
                "    contour = cv2.approxPolyDP(contour, 3, closed=True)\n",
                "    \n",
                "    if len(contour) < 3:\n",
                "        return None\n",
                "    \n",
                "    # Normalize coordinates\n",
                "    norm_contour = contour.reshape(-1, 2).astype(np.float32)\n",
                "    norm_contour[:, 0] /= image_size[1]\n",
                "    norm_contour[:, 1] /= image_size[0]\n",
                "    \n",
                "    # YOLO format: [class_id, x1, y1, x2, y2, ...]\n",
                "    seg_data = [0] + norm_contour.flatten().tolist()  # class_id=0 for tumor\n",
                "    return seg_data[:min(101, len(seg_data))]  # Limit to 50 polygon points\n",
                "\n",
                "# Prepare YOLO dataset structure\n",
                "yolo_dir = OUTPUT_DIR / 'yolo_brats'\n",
                "for split in ['train', 'val']:\n",
                "    (yolo_dir / split / 'images').mkdir(parents=True, exist_ok=True)\n",
                "    (yolo_dir / split / 'labels').mkdir(parents=True, exist_ok=True)\n",
                "\n",
                "# Convert 2D slices to YOLO format\n",
                "print('Converting 2D slices to YOLO format...')\n",
                "for split in ['train', 'val']:\n",
                "    img_dir = slices_dir / split / 'images'\n",
                "    mask_dir = slices_dir / split / 'masks'\n",
                "    \n",
                "    img_files = sorted(img_dir.glob('*.png'))\n",
                "    converted = 0\n",
                "    \n",
                "    for img_path in img_files:\n",
                "        # Copy image\n",
                "        dst_img = yolo_dir / split / 'images' / img_path.name\n",
                "        shutil.copy(img_path, dst_img)\n",
                "        \n",
                "        # Find corresponding mask\n",
                "        mask_path = mask_dir / (img_path.stem + '_mask.png')\n",
                "        \n",
                "        img = cv2.imread(str(img_path))\n",
                "        if img is not None and mask_path.exists():\n",
                "            seg_data = mask_to_yolo_seg(mask_path, img.shape)\n",
                "            \n",
                "            if seg_data:\n",
                "                # Write YOLO label file\n",
                "                label_path = yolo_dir / split / 'labels' / (img_path.stem + '.txt')\n",
                "                with open(label_path, 'w') as f:\n",
                "                    f.write(' '.join(map(str, seg_data)) + '\\n')\n",
                "                converted += 1\n",
                "    \n",
                "    print(f'  {split}: {converted} images converted')\n",
                "\n",
                "print('\\n✓ Dataset prepared for YOLO')"
